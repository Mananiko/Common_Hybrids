           <span class="textheader"><h2> Common Hybrids</h2></span> 
            <p class="text">This is a game of creating and assembling connections between players. This Game or a Non-game, is based on Georgian phenomenon called Kamikaze Loggia,structures built out of the block residential buildings by self-acknowledged architects, citizens of post soviet Georgia. Structures are feeble, dependent on and built upon each-other. Game shows the necessity of lost connections, that were so common in Georgian culture and at the same time touches the topic of free play(Isamu Noguchi), where everyone can decide upon the rules of how to make those structures. </p>
            <p class="text">Georgian society is well known for common spaces and shared activities. However, the political philosophy of recent times has shaped the social function of the architectural forms, creating an identical and disenchanted landscape with rows of residential apartments that were closed off from each other. It was meant to be built for citizens to accommodate them with all the modern necessities, shared urban spaces etc. Though, because of how and where they were situated, it caused closed, non-social interaction between residents.</p>
            <p class="text">Therefore, slick concrete buildings left by the Soviet Era, did not always consider space for interaction or the exchange and co-creation of common narratives. Before the 1990's historical residential buildings facilitated endless possibilities for interaction and moving through common space. However, after the 1990’s this common narrative shifted to Concrete residential blocks in the form of Kamikaze Loggia. This self-directed phenomenon gave citizens the possibility to express themselves and interact with the concept of common spaces in a more experimental fashion, but at the same time highlighting the difficulty in engineering connections between themselves or other parts of the building.</p>
             <p class="text">This Game is a Hybrid and poetic metaphor to show how players can play freely, decide: what, where and when they will build a structure and indirectly cause other players to adjust to those changes. This game is a  narration of the dependent connections between people, who might or might not know each other, but still be codependent whilst building “connections” in game.</p>
              <p class="text">So called “connections” are blocks-connectors, between buildings, as mentioned before it’s a metaphor that indirectly shows the possibility of codependency. At the same time it’s a Hybrid, as new structures are built not only by bloks, but also with random objects that a player can move, rotate, scale. Game resembles the map of Gldani, one of the well known residential apartment building suburbs. Buildings differ by color and function. This game should eventually transform into the real world, as a toy from build structur.</p>
