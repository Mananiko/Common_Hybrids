            <span class="textheader"><h4> How it works</h4></span> 
            <ol class="text"  style="list-style-type: disc;">
                <li >An Individual Player is sent a game, via invitation by me to play it. I create a group of 10 people, register them and send them details for log in.</li>
                <li>Game consists of Block Buildings that disassemble in the process of making. This happens by a player making any clicking on them. Blocks become connectors.</li>
                <li>By disassembled pieces, players can build a so-called bridg, between buildings.  Bridge represents connections.</li>
                <li>Every player is able to make only N(for now 100) amount of moves. </li>
                <li>Whilst playing random objects might appear on a certain spot. They can be scaled, moved, rotated.</li>
                <li>After finishing playing, the player can leave a comment for the next player. The game is ready for the next Player.</li>
            </ol>
